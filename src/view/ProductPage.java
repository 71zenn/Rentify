    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {
        // HARDCODED VALUES: User ID = 1, Product ID = 1 (John Wick), Qty = 1
        int userId = 1;
        int productId = 1;
        int quantity = 1;
        String actionType = "buy"; // default

        boolean success = cartController.addToCart(userId, productId, quantity, actionType);

        if (success) {
            javax.swing.JOptionPane.showMessageDialog(this, "Item added to cart successfully!");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Failed to add item to cart.", "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

