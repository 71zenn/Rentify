   private void loadRentalHistory() {
    int userId = UserSession.getUserId();

    if (userId == -1) {
        JOptionPane.showMessageDialog(this, "Session expired. Please login again.");
        // new Login().setVisible(true);
        dispose();
        return;
    }

    List<Rental> list = rentalDAO.getRentalHistory(userId);

    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // clear old rows

    if (list == null || list.isEmpty()) {
        // Optional: show empty row
        model.addRow(new Object[]{"No rented items", "-", "-", "-"});
        return;
    }

    for (Rental r : list) {
        String rentedOn = (r.getRentalDate() != null) ? sdf.format(r.getRentalDate()) : "N/A";

        String expiryOrReturn;
        if (r.getReturnDate() == null) {
            expiryOrReturn = "Active"; // or r.getExpiryDate() if you have it
        } else {
            expiryOrReturn = sdf.format(r.getReturnDate());
        }

        String status = r.getStatus(); // "ACTIVE" / "COMPLETED" / "EXPIRED"

        model.addRow(new Object[]{
                r.getItemName(),
                rentedOn,
                expiryOrReturn,
                status
        });
    }
}


